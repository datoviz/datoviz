#include <cstddef>

#include "_atomic.h"
#include "_macros.h"



/*************************************************************************************************/
/*  Atomic functions                                                                             */
/*************************************************************************************************/

void dvz_atomic_init(DvzAtomic* atomic) { ASSERT(atomic != NULL); }



void dvz_atomic_set(DvzAtomic* atomic, int32_t value)
{
    ASSERT(atomic != NULL);
    (*atomic) = value;
}



int32_t dvz_atomic_get(DvzAtomic* atomic)
{
    ASSERT(atomic != NULL);
    int32_t value = (*atomic);
    return value;
}
