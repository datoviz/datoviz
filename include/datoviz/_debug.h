/*************************************************************************************************/
/*  Macros                                                                                       */
/*************************************************************************************************/

#define DBG(x)  printf("%" PRIu64 "\n", (x));
#define DBGS(x) printf("%" PRId64 "\n", (x));
#define DBGF(x) printf("%.8f\n", (double)(x))
#define PRT(x)  printf("%s\n", (x))



/*************************************************************************************************/
/*  Debugging functions                                                                          */
/*************************************************************************************************/

static void dvz_show_base64(size_t size, const void* buffer)
{
    char* encoded = b64_encode((const unsigned char*)buffer, size);
    printf("base64 is: %s\n", encoded);
    free(encoded);
}



